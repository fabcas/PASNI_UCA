package entidades;

public class VwMaestroCarreras {
	
	private String CARR;
	private String Ordinal;
	private String COFA;
	private String NOMBRE;
	private String ABREV;
	private String ACTI;
	private String Pregrado;
	
	//Setters and Getters
	
	public String getCARR() {
		return CARR;
	}
	public void setCARR(String cARR) {
		CARR = cARR;
	}
	public String getOrdinal() {
		return Ordinal;
	}
	public void setOrdinal(String ordinal) {
		Ordinal = ordinal;
	}
	public String getCOFA() {
		return COFA;
	}
	public void setCOFA(String cOFA) {
		COFA = cOFA;
	}
	public String getNOMBRE() {
		return NOMBRE;
	}
	public void setNOMBRE(String nOMBRE) {
		NOMBRE = nOMBRE;
	}
	public String getABREV() {
		return ABREV;
	}
	public void setABREV(String aBREV) {
		ABREV = aBREV;
	}
	public String getACTI() {
		return ACTI;
	}
	public void setACTI(String aCTI) {
		ACTI = aCTI;
	}
	public String getPregrado() {
		return Pregrado;
	}
	public void setPregrado(String pregrado) {
		Pregrado = pregrado;
	}
	
	

}
